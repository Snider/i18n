package cmd

import (
	"fmt"
	"os"

	"github.com/spf13/cobra"
)

// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "i18n",
	Short: "i18n is a CLI for managing internationalization and localization.",
	Long: `i18n is a CLI for managing internationalization and localization.

It provides two primary functions:
  - A web server for translating messages in a web UI
  - A tool for managing locale files`,
	Run: func(cmd *cobra.Command, args []string) {
		fmt.Println("Hello from the i18n CLI!")
	},
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	if err := rootCmd.Execute(); err != nil {
		fmt.Println(err)
		os.Exit(1)
	}
}
